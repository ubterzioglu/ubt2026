import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const inputFile = resolve(__dirname, "../cvopt_participants_rows.json");
const outputFile = resolve(__dirname, "../migrate_cvopt_to_cv_review_requests.sql");

const participants = JSON.parse(readFileSync(inputFile, "utf-8"));

function normalizeWhatsapp(raw) {
  const num = raw.trim();

  if (num.startsWith("+")) {
    const cleaned = "+" + num.replace(/[^\d]/g, "");
    if (cleaned.startsWith("+090")) return "+9" + cleaned.substring(3);
    return cleaned;
  }

  const digitsOnly = num.replace(/\D/g, "");

  if (num.startsWith("0") && digitsOnly.length >= 12) {
    const withoutLeadingZero = digitsOnly.substring(1);
    if (withoutLeadingZero.startsWith("15") || withoutLeadingZero.startsWith("16") || withoutLeadingZero.startsWith("17") || withoutLeadingZero.startsWith("18")) {
      return "+49" + withoutLeadingZero;
    }
    return "+" + withoutLeadingZero;
  }

  if (num.startsWith("0") && digitsOnly.length === 11) {
    return "+90" + digitsOnly.substring(1);
  }

  if (digitsOnly.length === 10 && digitsOnly.startsWith("5")) {
    return "+90" + digitsOnly;
  }

  return "+" + digitsOnly;
}

function escapeSql(str) {
  return str.replace(/'/g, "''");
}

const values = participants.map((p) => {
  const id = p.id;
  const fullName = escapeSql(p.name);
  const whatsapp = normalizeWhatsapp(p.whatsapp);
  const linkedin = escapeSql(p.linkedin);
  const status = p.approved ? "approved" : "new";
  const cvReviewed = p.cv_ok ? "true" : "false";
  const linkedinReviewed = p.linkedin_ok ? "true" : "false";
  const createdAt = p.created_at;
  const updatedAt = p.updated_at;

  return `  ('${id}', '${fullName}', '${whatsapp}', '${linkedin}', '${status}', ${cvReviewed}, ${linkedinReviewed}, '${createdAt}', '${updatedAt}')`;
});

const sql = `-- Migration: cvopt_participants -> cv_review_requests
-- Generated by scripts/migrate-cvopt-participants.mjs
-- Run via Supabase Dashboard SQL Editor (service role privileges required)
-- ${participants.length} rows

INSERT INTO public.cv_review_requests
  (id, full_name, whatsapp_number, linkedin_url, status, cv_reviewed, linkedin_reviewed, created_at, updated_at)
VALUES
${values.join(",\n")}
ON CONFLICT (id) DO NOTHING;
`;

writeFileSync(outputFile, sql, "utf-8");

const normalized = participants.map((p) => ({
  name: p.name,
  before: p.whatsapp,
  after: normalizeWhatsapp(p.whatsapp)
}));

const changed = normalized.filter((n) => n.before !== n.after);

console.log(`Total participants: ${participants.length}`);
console.log(`SQL written to: ${outputFile}`);
console.log(`\nWhatsApp numbers normalized (${changed.length}):`);
changed.forEach((n) => console.log(`  ${n.name}: ${n.before} -> ${n.after}`));
